package com.qianfeng.springboot.bean;


public class DefaultInterest {

  private long recycleId;
  private long userId;
  private long recycleCapital;
  private long defaultInterest;


  public long getRecycleId() {
    return recycleId;
  }

  public void setRecycleId(long recycleId) {
    this.recycleId = recycleId;
  }


  public long getUserId() {
    return userId;
  }

  public void setUserId(long userId) {
    this.userId = userId;
  }


  public long getRecycleCapital() {
    return recycleCapital;
  }

  public void setRecycleCapital(long recycleCapital) {
    this.recycleCapital = recycleCapital;
  }


  public long getDefaultInterest() {
    return defaultInterest;
  }

  public void setDefaultInterest(long defaultInterest) {
    this.defaultInterest = defaultInterest;
  }

}
